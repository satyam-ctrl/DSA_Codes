package Lec1;

public class Assignment_Operator {
	public static void main(String[] args) {
		
		int x =2;
		System.out.println(x);
		
		x+=8;
		System.out.println(x);
		
		x-=4;
		System.out.println(x);
		
		x*=10;
		System.out.println(x);
		
		x/=6;
		System.out.println(x);
		
		x/=2;
		System.out.println(x);
		
		
		x%=3;
		System.out.println(x);
	}

}
